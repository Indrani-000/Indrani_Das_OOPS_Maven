package com.epam.week1;
import newyeargift.NewYearGift;
public class Sweet implements NewYearGift {
	int weight;int price;
	public Sweet (int weight,int price)
	{
		this.weight=weight;
		this.price=price;
	}
	public int getweight()
	{
		return getweight();
	}
	public int getprice()
	{
		return getprice();
	}
}
